var num = parseInt(prompt("Insira um número de 1 a 7:"));


if (num == 1) {
    alert(num + " - Domingo");
} else if (num == 2) {
    alert(num + " - Segunda-feira");
} else if (num == 3) {
    alert(num + " - Terça-feira");
} else if (num == 4) {
    alert(num + " - Quarta-feira");
} else if (num == 5) {
    alert(num + " - Quinta-feira");
} else if (num == 6) {
    alert(num + " - Sexta-feira");
} else if (num == 7) {
    alert(num + " - Sábado");
} else {
    alert("Valor inválido");
}
